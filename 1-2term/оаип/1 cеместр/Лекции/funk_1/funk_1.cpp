void fun1(int aa, int bb)
{
	aa--;   // в этой функции измененное значение локальных  
	bb++;   // переменных aa и bb не передаетс€ в main 
	printf("\nрезультат в ф-ции    fun1       aa = %d   bb=%d", aa, bb);
}

void fun2(int a, int b)
{
	a--;   // в этой функции измененное значение локальных  
	b++;   // переменных aa и bb не передаетс€ в main 
	printf("\nрезультат в ф-ции    fun2        a = %d   b=%d", a, b);
}

void fun3(int *a, int *b)
{
	(*a)++;      // в этой функции выполн€етс€ изменение значений переменных расположенных  
	(*b) += *a;  // по адресам которые переданы в функцию. —л-но измененное значение "видно" в main
	printf("\nрезультат в ф-ции    fun3  *(a) = %d  *(b)=%d", *a, *b);
}
void fun4(int &aa, int &bb)
{                // в этом случае идентификаторы aa и bb €вл€ютс€ синонимами имен переменных 
	aa++;        // a и b используемых в main (т.е. втрыми именами этих переменных)
	bb--;        // все изменени€ aa и bb отражаютс€ на значении переменных a и b в main
	printf("\nрезультат в ф-ции    fun4     aa = %d   bb=%d", aa, bb);
}