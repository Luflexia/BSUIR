#include <stdio.h>
#include <conio.h>
#include <windows.h>
#include <locale.h>
// функция вычисления (формирования) k элементов 
// последовательности  Фибоначчи  1 1 2 3 5 8 13 ...
int main()               
{
	setlocale(LC_ALL, "Russian");       // русификация вывода информации на экр
    int n1, n2 = 1, n3 = 1, i, k,kk;
	do                                 // цикл ввода числа членов последовательности 
	{	                               // с проверкой на корректность ввода
		system("CLS");                 // очистка экрана
		printf("введите число k = ");
		fflush(stdin);   // rewind(stdin); // чистка буфера ввода
		n1 = scanf_s("%d", &k);        // ввод размерности последовательности
	} while (!n1);                     // цикл повторяется пока n1==0
	n1 = 0; kk = 0;
	for (i = 0; i<k; i++)              // цикл формирования последовательности
	{
		n1 = i<2 ? 1 : n2 + n3;        // нахождение очередного элемента последолательности
		printf("%15d", n1);            // вывод найденного элемента
		kk++;                          // выведено очередное число
		if (kk == 7)                  // на экран выведено 10 элементов посл-сти
		{
			kk = 0;
			printf("\n");
		}
		n2 = n3;         // запоминаем старый n3     ( n2  n3 )                      
		n3 = n1;         // запоминаем новый  n3
	}
	printf("\n");
	return 0;
}